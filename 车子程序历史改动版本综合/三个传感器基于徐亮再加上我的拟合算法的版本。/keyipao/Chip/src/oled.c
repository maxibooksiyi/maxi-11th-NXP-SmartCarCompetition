	GPIOE_PDDR=GPIO_PDDR_PDD(GPIO_PIN(2)|GPIO_PIN(0)|GPIO_PIN(6)|GPIO_PIN(4));
	
  
	GPIOE_PDOR |=  GPIO_PDOR_PDO(GPIO_PIN(6));
	//LCD_CS=1;	//预制SLK和SS为高电平   	
	
