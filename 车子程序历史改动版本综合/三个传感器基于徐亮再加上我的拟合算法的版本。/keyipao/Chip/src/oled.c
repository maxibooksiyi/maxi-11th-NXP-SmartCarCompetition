void LCD_WrDat(byte data)
{
	byte i=8;
	//LCD_CS=0;;
	GPIOE_PDOR |=  GPIO_PDOR_PDO(GPIO_PIN(0));;;;
        asm("nop"); 
      GPIOE_PDOR &= ~GPIO_PDOR_PDO(GPIO_PIN(6));;;;
      asm("nop");    
  while(i--)
  {
    if(data&0x80){GPIOE_PDOR |=  GPIO_PDOR_PDO(GPIO_PIN(4));;;;}
    else{GPIOE_PDOR &= ~GPIO_PDOR_PDO(GPIO_PIN(4));;;;}
    GPIOE_PDOR |=  GPIO_PDOR_PDO(GPIO_PIN(6)); 
    asm("nop");;;;
		//asm("nop");            
    GPIOE_PDOR &= ~GPIO_PDOR_PDO(GPIO_PIN(6));;;;;    
    data<<=1;    
  }
	//LCD_CS=1;
}
void LCD_WrCmd(byte cmd)
{
	byte i=8;
	
	//LCD_CS=0;;
  GPIOE_PDOR &= ~GPIO_PDOR_PDO(GPIO_PIN(0));;;;;
  GPIOE_PDOR &= ~GPIO_PDOR_PDO(GPIO_PIN(6));;;;;
  //asm("nop");   
  while(i--)
  {
    if(cmd&0x80){GPIOE_PDOR |=  GPIO_PDOR_PDO(GPIO_PIN(4));;;;;}
    else{GPIOE_PDOR &= ~GPIO_PDOR_PDO(GPIO_PIN(4));;;;;;}
    GPIOE_PDOR |=  GPIO_PDOR_PDO(GPIO_PIN(6));;;;;
    asm("nop");;;;
		//asm("nop");             
    GPIOE_PDOR &= ~GPIO_PDOR_PDO(GPIO_PIN(6));;;;;    
    cmd<<=1;;;;;   
  } 	
	//LCD_CS=1;
}
void LCD_Set_Pos(byte x, byte y)
{ 
  LCD_WrCmd(0xb0+y);
  LCD_WrCmd(((x&0xf0)>>4)|0x10);
  LCD_WrCmd((x&0x0f)|0x01); 
} 
void LCD_Fill(byte bmp_data)
{
	byte y,x;
	
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			LCD_WrDat(bmp_data);
	}
}
